// ======================================================
//  الدوال المساعدة
// ======================================================

function getRandomMessage(category, gender) {
    gender = gender || 'male';
    var msgs = motivations[category] && motivations[category][gender];
    if (!msgs || msgs.length === 0) return '';
    return msgs[Math.floor(Math.random() * msgs.length)];
}

function getMotivationalMessage(gender) {
    return getRandomMessage('welcome', gender);
}

function getCorrectAnswerMessage(gender) {
    return getRandomMessage('correct', gender);
}

function getWrongAnswerMessage(gender) {
    return getRandomMessage('wrong', gender);
}

function getSessionEndMessage(gender, successRate) {
    if (successRate >= 80) return getRandomMessage('excellent', gender);
    if (successRate >= 60) return getRandomMessage('good', gender);
    return getRandomMessage('needsWork', gender);
}

function getReviewMessage(gender) {
    return getRandomMessage('review', gender);
}

function getRegistrationMessage(gender) {
    return getRandomMessage('registration', gender);
}

function getSubscribeMessage(gender) {
    return getRandomMessage('subscribe', gender);
}

function getStreakMessage(gender) {
    return getRandomMessage('streak', gender);
}

function getComebackMessage(gender) {
    return getRandomMessage('comeback', gender);
}
